# R	N	18	32: RN
# N	O	14	33: NO
# O	T	15	34: OT
# T	T	20	35: TT
# TO	B	27	36: TOB
# BE	O	29	37: BEO
# OR	T	31	38: ORT
# TOB	E	36	39: TOBE
# EO	R	30	40: EOR
# RN	O	32	41: RNO
# OT	 	34	 
